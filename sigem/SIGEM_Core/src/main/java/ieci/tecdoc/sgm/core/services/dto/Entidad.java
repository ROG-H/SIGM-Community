package ieci.tecdoc.sgm.core.services.dto;

public class Entidad {

	private String identificador;
	private String nombre;
	private String nombreCorto;
	private String nombreLargo;
	private String codigoINE;
	private String dir3;
	private String sia;
	private String deh;
	
	public String getIdentificador() {
		return identificador;
	}
	public void setIdentificador(String identificador) {
		this.identificador = identificador;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNombreLargo() {
		return nombreLargo;
	}
	public void setNombreLargo(String nombreLargo) {
		this.nombreLargo = nombreLargo;
	}
	public String getCodigoINE() {
		return codigoINE;
	}
	public void setCodigoINE(String codigoINE) {
		this.codigoINE = codigoINE;
	}
	public String getDir3() {
		return dir3;
	}
	public void setDir3(String dir3) {
		this.dir3 = dir3;
	}
	public String getSia() {
		return sia;
	}
	public void setSia(String sia) {
		this.sia = sia;
	}
	public String getDeh() {
		return deh;
	}
	public void setDeh(String deh) {
		this.deh = deh;
	}
	public String getNombreCorto() {
		return nombreCorto;
	}
	public void setNombreCorto(String nombreCorto) {
		this.nombreCorto = nombreCorto;
	}
			
}
