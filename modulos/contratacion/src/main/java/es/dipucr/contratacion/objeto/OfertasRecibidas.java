package es.dipucr.contratacion.objeto;

public class OfertasRecibidas {
	private String numOfertasRecibidas;
	private String ofertaMasAlta;
	private String ofertaMasBaja;
	public String getNumOfertasRecibidas() {
		return numOfertasRecibidas;
	}
	public void setNumOfertasRecibidas(String numOfertasRecibidas) {
		this.numOfertasRecibidas = numOfertasRecibidas;
	}

	public String getOfertaMasBaja() {
		return ofertaMasBaja;
	}
	public void setOfertaMasBaja(String ofertaMasBaja) {
		this.ofertaMasBaja = ofertaMasBaja;
	}
	public String getOfertaMasAlta() {
		return ofertaMasAlta;
	}
	public void setOfertaMasAlta(String ofertaMasAlta) {
		this.ofertaMasAlta = ofertaMasAlta;
	}
	
}
