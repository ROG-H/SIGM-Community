package es.dipucr.sigem.api.rule.procedures.expropiaciones;

public class LevantamientoActaPrevia {
	private String numexp = "";
	private String fincaNum = "";
	private String fecha = "";
	private String hora = "";
	private String propietarios = "";
	private String poligono = "";
	private String parcela = "";
	private String municipioParcela = "";
	private String superficieParcela = "";
	private String superficieOcupar = "";
	private String calificacion = "";
	public String getFincaNum() {
		return fincaNum;
	}
	public void setFincaNum(String fincaNum) {
		this.fincaNum = fincaNum;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getPropietarios() {
		return propietarios;
	}
	public void setPropietarios(String propietarios) {
		this.propietarios = propietarios;
	}
	public String getPoligono() {
		return poligono;
	}
	public void setPoligono(String poligono) {
		this.poligono = poligono;
	}
	public String getParcela() {
		return parcela;
	}
	public void setParcela(String parcela) {
		this.parcela = parcela;
	}
	public String getMunicipioParcela() {
		return municipioParcela;
	}
	public void setMunicipioParcela(String municipioParcela) {
		this.municipioParcela = municipioParcela;
	}
	public String getSuperficieParcela() {
		return superficieParcela;
	}
	public void setSuperficieParcela(String superficieParcela) {
		this.superficieParcela = superficieParcela;
	}
	public String getSuperficieOcupar() {
		return superficieOcupar;
	}
	public void setSuperficieOcupar(String superficieOcupar) {
		this.superficieOcupar = superficieOcupar;
	}
	public String getCalificacion() {
		return calificacion;
	}
	public void setCalificacion(String calificacion) {
		this.calificacion = calificacion;
	}
	public String getNumexp() {
		return numexp;
	}
	public void setNumexp(String numexp) {
		this.numexp = numexp;
	}

}
