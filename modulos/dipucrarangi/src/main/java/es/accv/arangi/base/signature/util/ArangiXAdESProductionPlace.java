package es.accv.arangi.base.signature.util;

/**
 * Contiene los campos del elemento SignatureProductionPlace de XAdES
 * 
 * @author <a href="mailto:jgutierrez@accv.es">José Manuel Gutiérrez Núñez</a>
 */
public class ArangiXAdESProductionPlace {

	private String city;
	private String state;
	private String postalCode;
	private String country;
	public ArangiXAdESProductionPlace(String city, String state,
			String postalCode, String country) {
		super();
		this.city = city;
		this.state = state;
		this.postalCode = postalCode;
		this.country = country;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPostalCode() {
		return postalCode;
	}
	public void setPostalCode(String postalCode) {
		this.postalCode = postalCode;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	
	@Override
	public String toString() {
		return city + " | " + state + " | " + postalCode + " | " + country;
	}
	
}
