package es.dipucr.integracion.beans;

public class ResultProcedimientoBean {
	
	
	private String codProcemiento = null;
	private String nombreProcedimiento = null;
	
	
	public ResultProcedimientoBean() {}


	public String getCodProcemiento() {
		return codProcemiento;
	}


	public void setCodProcemiento(String codProcemiento) {
		this.codProcemiento = codProcemiento;
	}


	public String getNombreProcedimiento() {
		return nombreProcedimiento;
	}


	public void setNombreProcedimiento(String nombreProcedimiento) {
		this.nombreProcedimiento = nombreProcedimiento;
	} 
	
	
	
	

}
