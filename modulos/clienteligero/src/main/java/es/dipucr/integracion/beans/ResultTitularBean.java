package es.dipucr.integracion.beans;

public class ResultTitularBean {
	
	private String tipoDocumentacion = null;
	private String documentacion = null;
	private String nombreCompleto = null;
	private String nombre = null;
	private String apellido1 = null;
	private String apellido2 = null;
	
	
	public ResultTitularBean() {}


	public String getTipoDocumentacion() {
		return tipoDocumentacion;
	}


	public void setTipoDocumentacion(String tipoDocumentacion) {
		this.tipoDocumentacion = tipoDocumentacion;
	}


	public String getDocumentacion() {
		return documentacion;
	}


	public void setDocumentacion(String documentacion) {
		this.documentacion = documentacion;
	}


	public String getNombreCompleto() {
		return nombreCompleto;
	}


	public void setNombreCompleto(String nombreCompleto) {
		this.nombreCompleto = nombreCompleto;
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public String getApellido1() {
		return apellido1;
	}


	public void setApellido1(String apellido1) {
		this.apellido1 = apellido1;
	}


	public String getApellido2() {
		return apellido2;
	}


	public void setApellido2(String apellido2) {
		this.apellido2 = apellido2;
	}
	
	
	
	

}
