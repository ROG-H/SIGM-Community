package es.dipucr.integracion.beans;

public class ResultTransmisionBean {
	
	
	private String codigoCertificado = null;
	private String idSolicitud = null;
	private String idTransmision = null;
	private String fechaGeneracion = null;
	
	
	public ResultTransmisionBean() {}


	public String getCodigoCertificado() {
		return codigoCertificado;
	}


	public void setCodigoCertificado(String codigoCertificado) {
		this.codigoCertificado = codigoCertificado;
	}


	public String getIdSolicitud() {
		return idSolicitud;
	}


	public void setIdSolicitud(String idSolicitud) {
		this.idSolicitud = idSolicitud;
	}


	public String getIdTransmision() {
		return idTransmision;
	}


	public void setIdTransmision(String idTransmision) {
		this.idTransmision = idTransmision;
	}


	public String getFechaGeneracion() {
		return fechaGeneracion;
	}


	public void setFechaGeneracion(String fechaGeneracion) {
		this.fechaGeneracion = fechaGeneracion;
	} 
	
	
	
	
	

}
